# ce programme permet de determiner si le nombre est pair ou impaire
#en fonction de la demande de l'utilisateur
nombre = int(input("entrez votre nombre ? "))
k = nombre // 2
if nombre ==2 * k:
    print("votre nombre est pair")
else:
    print("c'est impaire")

